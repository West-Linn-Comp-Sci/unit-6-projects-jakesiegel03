
public class Person
{
    // instance variables - replace the example below with your own
    private int myAge;
    private String myName;
    
    public Person(int myAge1, String myName1)
    {
        myAge = myAge1;
        myName = myName1;
    }
    
    
    public int getAge(){
        return myAge;
    }
    
    public String getName(){
        return myName;
    }

    
}
